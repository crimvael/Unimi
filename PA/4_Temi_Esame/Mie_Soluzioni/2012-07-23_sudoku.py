def check_lines(sodoku): return sum([sum(set(lines)) for lines in sodoku]) == 405
def check_columns(sodoku): return sum([sum(set([sodoku[r][c] for r in range(9)])) for c in range(9)]) == 405
def check_squares(sodoku): return sum([sum(set([sodoku[i+t][j+k] for k in range(3) for t in range(3)])) for j in range(0,9,3) for i in range(0,9,3)]) == 405
def is_sudoku(sodoku): return check_lines(sodoku) and check_columns(sodoku) and check_squares(sodoku)

